inline uint16 HaltonSampler::invert(const uint16 base, const uint16 digits,
	uint16 index, const std::vector<uint16>& perm)
{
	uint16 result = 0;
	for (uint16 i = 0; i < digits; ++i)
	{
		result = result * base + perm[index % base];
		index /= base;
	}
	return result;
}


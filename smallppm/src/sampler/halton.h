inline void HaltonSampler::init_faure()
{
	const uint32 max_base = 1619u;
	std::vector<std::vector<uint16> > perms(max_base + 1);
	for (uint32 k = 1; k <= 3; ++k) // Keep identity permutations for base 1, 2, 3.
	{
		perms[k].resize(k);
		for (uint32 i = 0; i < k; ++i)
			perms[k][i] = i;
	}
	for (uint32 base = 4; base <= max_base; ++base)
	{
		perms[base].resize(base);
		const uint32 b = base / 2;
		if (base & 1) // odd
		{
			for (uint32 i = 0; i < base - 1; ++i)
				perms[base][i + (i >= b)] = perms[base - 1][i] + (perms[base - 1][i] >= b);
			perms[base][b] = b;
		}
		else // even
		{
			for (uint32 i = 0; i < b; ++i)
			{
				perms[base][i] = 2 * perms[b][i];
				perms[base][b + i] = 2 * perms[b][i] + 1;
			}
		}
	}
	init_tables(perms);
}

void HaltonSampler::init_random(Rng &rand)
{
	const uint32 max_base = 1619u;
	std::vector<std::vector<uint16> > perms(max_base + 1);
	for (uint32 k = 1; k <= 3; ++k) // Keep identity permutations for base 1, 2, 3.
	{
		perms[k].resize(k);
		for (uint32 i = 0; i < k; ++i)
			perms[k][i] = i;
	}
	for (uint32 base = 4; base <= max_base; ++base)
	{
		perms[base].resize(base);
		for (uint32 i = 0; i < base; ++i)
			perms[base][i] = i;
		//std::random_shuffle(perms[base].begin(), perms[base].end(), rand);
		std::shuffle(perms[base].begin(), perms[base].end(), rand.GetRngEngine());
	}
	init_tables(perms);
}

